public class App {
    public static void main(String[] args) throws Exception {
        // Ternary operator example
        int score = 10000;
        String result = (score > 5000) ? "You win!" : "You lose!";
        System.out.println(result);

        // ternary operator simply is that !
        // it is a shorthand for an if-else statement.
        // It can be used to assign a value based on a condition.
        // The syntax is: condition ? value_if_true : value_if_false
    }
}
